    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(jComboBox1.getSelectedItem() == "Choisir..."){
              
              jTextField1.setText("");
              jComboBox2.setSelectedItem(null);
              jTextField2.setText(""); 
              
        }
        else{
        try{
          Class.forName(driverName);
          Connection con = null;
          con = DriverManager.getConnection(url,username,password);
          Statement s = con.createStatement();
            String sql = "SELECT * FROM medicament m INNER JOIN catalogue c ON c.idcatalogue = m.idcatalogue"
                    + " WHERE c.idcatalogue = '"+medicament[jComboBox1.getSelectedIndex()]+"'";
          jComboBox2.removeAllItems();
          ResultSet res = s.executeQuery(sql);
          while(res.next()){

              jComboBox2.addItem(res.getString("m.idmedicament"));
              jTextField2.setText(res.getString("c.lib"));
              
          }
        }
        catch(ClassNotFoundException e){
            JOptionPane.showMessageDialog(null,"err de pilote"); 
        }
        catch(SQLException s){
            JOptionPane.showMessageDialog(null,"err de connexion"); 
        }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

